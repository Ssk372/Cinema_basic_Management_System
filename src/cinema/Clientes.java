    public void generarTabla()
{
    
DBClientes c1 = new DBClientes();
List<EncapsulamientoCliente> Listas= c1.MostrarCliente();
DefaultTableModel modeloTabla = (DefaultTableModel) tblcliente.getModel();
while (modeloTabla.getRowCount()>0){
modeloTabla.removeRow(0);

}
for(EncapsulamientoCliente obCliente : Listas){

    Object[] DatosFila=
    {
obCliente.getId(),
obCliente.getNombres(),
obCliente.getApellidos(),
obCliente.getDireccion(),
obCliente.getTelefono(),
obCliente.getCorreo(),
obCliente.getNivel()
    };
modeloTabla.addRow(DatosFila);

}
tblcliente.setModel(modeloTabla);
}
